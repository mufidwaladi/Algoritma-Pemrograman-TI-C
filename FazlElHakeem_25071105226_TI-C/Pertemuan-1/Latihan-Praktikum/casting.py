#casting (konversi tipe data) dalam python dilakukan menggunakan fungsi konstruktor
# int() : mengubah menjadi bilangan bulat
# float() : mengubah menjadi float atau desimal
# str() : membuat string dari berbagai macam tipe data

#-int()-
x = int(3.5)
y = int('45')
print(type(x))
print(type(y))

#-float()-
x = float(10)
y = float('45')
print(type(x))
print(type(y))

#-str()-
x = str(3.5)
y = str(567)
print(type(x))
print(type(y))
#===PYTHON FUNCTION===

#dalam python fungsi didefenisikan dengan keyword 'def' diikuti nama fungsi dan tanda kurung
def my_function():
   print("Hello from a function")

#-Memanggil Fungsi-
#Untuk memanggil sebuah fungsi, tuliskan namanya diikuti tanda kurung
def my_function():
  print("Hello from a function")

my_function()

#nama fungsi mengikuti aturan yang sama seperti nama variabel

#-Return Value-
#fungsi dapat mengembalikan sebuah nilai dengan statement 'return'
def get_greeting():
  return "Hello from a function"

message = get_greeting()
print(message)
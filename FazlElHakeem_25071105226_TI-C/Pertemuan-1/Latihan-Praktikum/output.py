#===TEXT===

#kita mengggunakan fungsi print() untuk mencetak text
print("Hello world")

#text dalam python harus berada dalam tanda kutip baik yang ganda("") ataupun yang tunggal('')
print("Python is easy")
print('Python is easy')

#-print text tanpa baris baru-
#pada dasarnya fungsi print() diakhiri dengan baris baru, tapi kita bisa mencetak banyak kata dalam satu baris menggunakan end parameter
print("hallo", end=" ")
print("cetak di baris yang sama")

#===NUMBERS===

#untuk mencetak angka tidak perlu menggunakan tanda kutip
print(303)
print(1945)

#kita juga bisa melakukan perhitungan matematika di dalam fungsi print()
print(3+4)
print(100-35)

#untuk menggabungkan text dan angka dalam satu print() dapat menggunakan tanda koma
print(17, "Agustus", 1945)